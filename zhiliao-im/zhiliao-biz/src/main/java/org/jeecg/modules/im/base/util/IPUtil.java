    public static String getCityInfo(String ip) {
        try {
            String dbPath = ResourceUtils.getFile(ResourceUtils.CLASSPATH_URL_PREFIX + "ip2region.xdb").getPath();
            byte[] vIndex= Searcher.loadVectorIndexFromFile(dbPath);
            Searcher searcher = Searcher.newWithVectorIndex(dbPath, vIndex);
            long sTime = System.nanoTime();
            String region = searcher.search(ip);
            long cost = TimeUnit.NANOSECONDS.toMicros((long) (System.nanoTime() - sTime));
            System.out.printf("{region: %s, ioCount: %d, took: %d μs}\n", region, searcher.getIOCount(), cost);
            searcher.close();
            return region;
        } catch (Exception e) {
            log.error("get ip city info error:",e);
            return "-";
        }
    }
